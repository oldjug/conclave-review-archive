//! Built-in 5×7 monospace bitmap font.
//!
//! Covers digits, uppercase A-Z, lowercase a-z, space, and a small
//! punctuation set — enough to render "Score: 42", FPS counters,
//! labels in dial gauges, etc. Each glyph is 7 bytes, one byte per
//! row, with the low 5 bits storing the on/off mask (bit 4 = left
//! pixel, bit 0 = right pixel). Bytes are top-to-bottom.
//!
//! Glyph data was hand-traced — V2 will swap in a real TrueType
//! loader through cv_text once we have that crate built.

pub const GLYPH_W: u32 = 5;
pub const GLYPH_H: u32 = 7;

/// Look up a glyph for `c`. Returns the 7-byte row mask, or all
